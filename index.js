const spacetime = require('spacetime')
const calendar = require('./src')
// const inputs = require('somehow-input')

let start = 'Jan 1 2019'
let end = spacetime('Dec 28 2019')
let cal = calendar.months(start, end, { monthName: true })
cal.width('20px')
cal.height('20px')
cal.color('june 14th 2019', 'June 23rd 2019', '#cc7066')
document.querySelector('#months').innerHTML = cal.build()

cal = calendar.weeks(start, end)
cal.width('12px')
cal.height('12px')
cal.label('Feb 1 2019', 'F')
cal.label('Mar 1 2019', 'M')
cal.label('Apr 1 2019', 'A')
cal.label('May 1 2019', 'M')
cal.label('Jun 1 2019', 'J')
cal.label('July 1 2019', 'J')
cal.label('Aug 1 2019', 'A')
cal.label('Sep 1 2019', 'S')
cal.label('Oct 1 2019', 'O')
cal.label('Nov 1 2019', 'N')
cal.label('Dec 1 2019', 'D')
cal.underline('june 14th 2019', 'June 23rd 2019', '#cc7066')

document.querySelector('#weeks').innerHTML = cal.build()

cal = calendar.timeline(start, end)
cal.width('15px')
cal.height('15px')
cal.color('june 14th 2019', 'June 23rd 2019', '#cc7066')

document.querySelector('#timeline').innerHTML = cal.build()
